package com.example.appsong.model;

public class Artist {

    private String artist;
    private String numberOfAlbum;
    private String numberOfSong;

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getNumberOfAlbum() {
        return numberOfAlbum;
    }

    public void setNumberOfAlbum(String numberOfAlbum) {
        this.numberOfAlbum = numberOfAlbum;
    }

    public String getNumberOfSong() {
        return numberOfSong;
    }

    public void setNumberOfSong(String numberOfSong) {
        this.numberOfSong = numberOfSong;
    }
}
